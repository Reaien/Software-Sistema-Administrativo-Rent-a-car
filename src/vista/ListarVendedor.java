    private void btnBuscarVendedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarVendedorActionPerformed
        DefaultTableModel modeloV = (DefaultTableModel) this.tblVendedor.getModel();
        modeloV.setRowCount(0); //limpiar tabla y mostrar solo 1 resultado
        
        String rutVendedor, digVerVen, nombreVendedor,fonoVendedor, mailVendedor, direccionVendedor; 

        rutVendedor = this.txtRutVendedor.getText();
        
        Validador v = new Validador();
        if (!v.validaVacio(rutVendedor)) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un rut", "Validación", JOptionPane.WARNING_MESSAGE);
            return;            
        }

        
        Vendedor vendedor = rgv.buscarVendedor(rutVendedor);
        digVerVen = vendedor.getDigVer();
        nombreVendedor = vendedor.getNombreVendedor();
        fonoVendedor = vendedor.getFonoVendedor();
        mailVendedor = vendedor.getMailVendedor();
        direccionVendedor = vendedor.getDireccionVendedor();
        
        //rellenar fila de la jtable
        modeloV.addRow(new Object[]{rutVendedor, digVerVen, nombreVendedor,fonoVendedor, mailVendedor, direccionVendedor});

        
    }//GEN-LAST:event_btnBuscarVendedorActionPerformed

    private void btnEditVendedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditVendedorActionPerformed
        int fila = this.tblVendedor.getSelectedRow(); //validar fila seleccionada para editar
        if (fila < 0) {
            JOptionPane.showMessageDialog(this, "Debe Seleccionar una Fila", "Validación", JOptionPane.WARNING_MESSAGE);
            this.tblVendedor.requestFocus();
            return;
        } else{
            
            Vendedor vendedor = new Vendedor();
            
            vendedor.setRutVendedor((String) this.tblVendedor.getValueAt(fila, 0));
            vendedor.setDigVer((String) this.tblVendedor.getValueAt(fila, 1));
            vendedor.setNombreVendedor((String) this.tblVendedor.getValueAt(fila, 2));
            vendedor.setFonoVendedor((String) this.tblVendedor.getValueAt(fila, 3));
            vendedor.setMailVendedor((String) this.tblVendedor.getValueAt(fila, 4));
            vendedor.setDireccionVendedor((String) this.tblVendedor.getValueAt(fila, 5));
            
            
            ModificarVendedor mv = new ModificarVendedor(vendedor);
            mv.setVisible(true);
            this.setVisible(false);            
        }        
        
        
        

    }//GEN-LAST:event_btnEditVendedorActionPerformed

