    private void btnDeleteVendedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteVendedorActionPerformed
        int fila = this.tblVendedor.getSelectedRow();
        //comprobar que se selecciono una fila 
        if (fila < 0) {
            JOptionPane.showMessageDialog(this, "Debe Seleccionar una Fila", "Validación", JOptionPane.WARNING_MESSAGE);
            this.tblVendedor.requestFocus();
            return;
        } else { //de haberse seleccionado se comprobará rut
            String rutVendedor = (String) this.tblVendedor.getValueAt(fila, 0);
            if (rgv.eliminarVendedor(rutVendedor)) {
                JOptionPane.showMessageDialog(this, "Se eliminó correctamente el vendedor con rut: " + rutVendedor, "Información", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "No se eliminó el vendedor con rut: " + rutVendedor, "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnDeleteVendedorActionPerformed

