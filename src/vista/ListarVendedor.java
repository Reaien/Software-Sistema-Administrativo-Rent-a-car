    public ListarVendedor() {
        initComponents();
        this.setLocationRelativeTo(null);
        
        //llenar jtable al inicio con la tabla vendedor
        String rutVendedor, digVerVen, nombreVendedor,fonoVendedor, mailVendedor, direccionVendedor; 
        DefaultTableModel modeloV = (DefaultTableModel) this.tblVendedor.getModel();
        
        modeloV.setRowCount(0);
        rutVendedor = this.txtRutVendedor.getText();
        if (rutVendedor.isEmpty()) {
            ArrayList<Vendedor> lista = rgv.llenarTableVendedores();
            for(Vendedor v: lista){
                rutVendedor = v.getRutVendedor();
                digVerVen = v.getDigVer();
                nombreVendedor = v.getNombreVendedor();
                fonoVendedor = v.getFonoVendedor();
                mailVendedor = v.getMailVendedor();
                direccionVendedor = v.getDireccionVendedor();
                
                modeloV.addRow(new Object[]{rutVendedor, digVerVen, nombreVendedor,fonoVendedor, mailVendedor, direccionVendedor});
            }
        }
        
    }

